    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        DB::beginTransaction();
        try {
            $oldIds = Category::pluck('id')->toArray();

            $postIds = Category::saveData($request->data);

            $deleteIds = array_diff($oldIds, $postIds);

            if (!empty($deleteIds)) {
                Category::whereIn('id', $deleteIds)->delete();
            }
            DB::commit();

            session()->flash('success', 'Save success');

            return response()->json(['success' => true]);
        } catch (\Exception $e) {
            DB::rollback();

            session()->flash('failure', 'Save failure');

            return response()->json(['success' => false]);
        }
    }

